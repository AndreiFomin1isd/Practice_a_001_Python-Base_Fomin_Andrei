# Сначала напишем функцию для нахождения минимума двух чисел
def min(a, b):
    
    if a < b:
        return a
    else:
        return b

# Функция для нахождения минимума трех чисел
def min3(a, b, c):
  
    # Находим минимум первых двух чисел
    min_ab = min(a, b)
    # Находим минимум между результатом и третьим числом
    result = min(min_ab, c)
    
    return result

# Функция для нахождения минимума четырех чисел
def min4(a, b, c, d):
  
    # Находим минимум первых трех чисел
    min_abc = min3(a, b, c)
    # Находим минимум между результатом и четвертым числом
    result = min(min_abc, d)
    
    return result

# Считываем три целых числа для демонстрации работы min3
print("Введите три числа для поиска минимума:")
a = int(input())
b = int(input())
c = int(input())

print(f"Минимум из трех чисел: {min3(a, b, c)}")

# Считываем четыре целых числа для демонстрации работы min4
print("\n Введите четыре числа для поиска минимума:")
x = int(input())
y = int(input())
z = int(input())
w = int(input())

print(f"Минимум из четырех чисел: {min4(x, y, z, w)}")